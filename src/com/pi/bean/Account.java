package com.pi.bean;

public class Account {
    private int ACCOUNTID;
    private int PID;
    private String ACCOUNTKESHI;
    private String ACCOUNTPOWER;
    private String ACCOUNTPASSWORD;

    public int getACCOUNTID() {
        return ACCOUNTID;
    }

    public void setACCOUNTID(int ACCOUNTID) {
        this.ACCOUNTID = ACCOUNTID;
    }

    public int getPID() {
        return PID;
    }

    public void setPID(int PID) {
        this.PID = PID;
    }

    public String getACCOUNTKESHI() {
        return ACCOUNTKESHI;
    }

    public void setACCOUNTKESHI(String ACCOUNTKESHI) {
        this.ACCOUNTKESHI = ACCOUNTKESHI;
    }

    public String getACCOUNTPOWER() {
        return ACCOUNTPOWER;
    }

    public void setACCOUNTPOWER(String ACCOUNTPOWER) {
        this.ACCOUNTPOWER = ACCOUNTPOWER;
    }

    public String getACCOUNTPASSWORD() {
        return ACCOUNTPASSWORD;
    }

    public void setACCOUNTPASSWORD(String ACCOUNTPASSWORD) {
        this.ACCOUNTPASSWORD = ACCOUNTPASSWORD;
    }
}
